package string_Class_allMethod;
//Int indexOf(String str)
public class S10 {
	public static void main(String[] args) {
		String s="Ram is a good boy";
		//to find the index
		int a=s.indexOf("is");
		int b=s.indexOf("a");
		int c=s.indexOf("good");
		System.out.println(a);//4 index
		System.out.println(b);//1 index
		System.out.println(c);//9 index
	}
	
	
}
