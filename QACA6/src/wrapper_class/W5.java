package wrapper_class;
//UNBoxing:- The process of converting, a primitive type into Non-primitive type is known as Boxing..and also known as auto unboxing.
public class W5 {
	public static void main(String[] args) {
	Integer a=20;
	int b=a;// non-primitive to primitive type..
	System.out.println(b);
	double c=a;
	System.out.println(c);
	float d=a;
	System.out.println(d);
	long e=a;
	System.out.println(e);
	}
}
